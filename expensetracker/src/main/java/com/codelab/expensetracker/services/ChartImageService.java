package com.codelab.expensetracker.services;

public class ChartImageService {
    private String chartImgData;
    private String chartType;
    private String tableImgData;

    // Getters and Setters
    public String getChartImgData() {
        return chartImgData;
    }

    public void setChartImgData(String chartImgData) {
        this.chartImgData = chartImgData;
    }

    public String getChartType() {
        return chartType;
    }

    public void setChartType(String chartType) {
        this.chartType = chartType;
    }

    public String getTableImgData() {
        return tableImgData;
    }

    public void setTableImgData(String tableImgData) {
        this.tableImgData = tableImgData;
    }
}



